static void dump(unsigned char *regs, unsigned count)
{
	unsigned i;

	i = 0;
	Debug::printf("\t");
	for(; count != 0; count--)
	{
		Debug::printf("0x%02X,", *regs);
		i++;
		if(i >= 8)
		{
			i = 0;
			Debug::printf("\n\t");
		}
		else
			Debug::printf(" ");
		regs++;
	}
	Debug::printf("\n");
}
