static void set_byte(unsigned my_src, unsigned off, unsigned val) {
	*(uint8_t *)((my_src << 4) + off) = val;
}
// Remake of vpokeb for our franework
static void byte_to_screen(unsigned off, unsigned val) {
	set_byte(get_fb_seg(), off, val);
}
